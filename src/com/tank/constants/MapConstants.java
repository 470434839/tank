	public static int randomDraw() {
		int x = 0;
		double d = Math.random();
		if (d < 0.2) {
			x = 1;
		} else if (d < 0.25) {
			x = 2;
		} else if (d < 0.35) {
			x = 3;
		} else if (d < 0.45) {
			x = 4;
		} else if (d < 0.5) {
			x = 5;
		} 
		return x;
	}

